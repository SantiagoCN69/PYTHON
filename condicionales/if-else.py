# Ejemplo básico de if-else en Python
# El if-else permite ejecutar diferentes bloques de código dependiendo si una condición es verdadera o falsa.

numero = -7

if numero > 0:
    # Este bloque se ejecuta si la condición es verdadera
    print("El número es positivo")
else:
    # Este bloque se ejecuta si la condición no se cumple
    print("El número es cero o negativo")

# Otro ejemplo simple:
edad = 18
if edad >= 18:
    print("Eres mayor de edad")
else:
    print("Eres menor de edad")