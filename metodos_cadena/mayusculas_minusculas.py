# EXPLICACION METODOS CADENA MAYUSCULAS/MINUSCULAS: Ejemplos de cómo convertir cadenas a mayúsculas, minúsculas, capitalizar y título en Python
# Métodos para convertir cadenas a mayúsculas y minúsculas en Python

texto = "hola mundo"

# upper(): convierte todo a mayúsculas
print(texto.upper())  # HOLA MUNDO

# lower(): convierte todo a minúsculas
print(texto.lower())  # hola mundo

# capitalize(): solo la primera letra en mayúscula
print(texto.capitalize())  # Hola mundo

# title(): primera letra de cada palabra en mayúscula
print(texto.title())  # Hola Mundo
