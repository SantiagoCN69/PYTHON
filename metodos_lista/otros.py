# Otros métodos útiles de listas en Python

lista = [1, 2, 3, 4, 5]

# copy(): crea una copia de la lista
copia = lista.copy()
print(copia)  # [1, 2, 3, 4, 5]

# clear(): elimina todos los elementos de la lista
lista.clear()
print(lista)  # []

# len(): obtiene la cantidad de elementos
print(len(copia))  # 5
